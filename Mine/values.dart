main() {
  // Strings can be appended using +
  print("dart" + "lang");

  // Integers and Floats
  print("1+1=${1 + 1}");
  print("7.0/3.0 =${7.0 / 3.0}");

  // Booleans
  print('\nboolean true && false :');
  print(true && false);

  print('\nboolean true || false :');
  print(false || true);

  print('\nboolean !true :');
  print(!true);
}
